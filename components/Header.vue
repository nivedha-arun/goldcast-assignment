export default {
    mounted(){
        function isElementInViewport(el) {
            var rect = el.getBoundingClientRect();
            var windowHeight = window.innerHeight || document.documentElement.clientHeight;
            var elementHeight = el.clientHeight || rect.height || el.getBoundingClientRect().height;

            return (
            rect.top <= windowHeight - elementHeight / 2 &&
            rect.bottom >= elementHeight / 2
            );
        }

        function revealOnScrollWithDelay() {
        var elements = document.querySelectorAll(".revealUp");
        elements.forEach(function (elem, index) {
            if (isElementInViewport(elem) && !elem.classList.contains("reveal")) {
            setTimeout(function () {
                elem.classList.add("reveal");
            }, index); 
            }
        });
        }

        document.addEventListener("DOMContentLoaded", function() {
            revealOnScrollWithDelay();
        });

        window.addEventListener("scroll", revealOnScrollWithDelay);
    }
}
